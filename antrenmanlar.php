        <div class="section">
            <h2>Split Programı</h2>
<p>Bu program, haftada 5 gün antrenman yapmayı içerir.</p>
<p>Her antrenman günü farklı kas gruplarına odaklanır.</p>
<p>Isınma ve soğuma egzersizlerini her antrenmanın başına ve sonuna ekleyin.</p>
<p>Her egzersizin 3-4 set ve 8-12 tekrar aralığında yapılması önerilir.</p>
<p>Ağırlıkları, doğru formu koruyarak zorlanabileceğiniz şekilde seçin.</p>
<p>Antrenmanlar arasında 60-90 saniye dinlenme yapın.</p>

<h3>Antrenman Programı</h3>

<h4>1. Gün: Göğüs ve Triceps</h4>
<p>Bench press: 4 set x 8-12 tekrar</p>
<p>Incline dumbbell press: 3 set x 10-12 tekrar</p>
<p>Decline dumbbell press: 3 set x 10-12 tekrar</p>
<p>Cable crossovers: 3 set x 12-15 tekrar</p>
<p>Triceps pushdown: 3 set x 12-15 tekrar</p>
<p>Skullcrushers: 3 set x 10-12 tekrar</p>
 <!-- Mobil reklam alanı -->
 <div class="mobile-ad"></div>
<h4>2. Gün: Sırt ve Biceps</h4>
<p>Barfiks (veya Lat pulldown): 4 set x maksimum tekrar</p>
<p>Bent-over rows: 4 set x 8-12 tekrar</p>
<p>Seated cable rows: 3 set x 10-12 tekrar</p>
<p>Face pulls: 3 set x 15-20 tekrar</p>
<p>Biceps curl (çeşitli versiyonlar): 4 set x 10-12 tekrar</p>
<p>Hammer curl: 3 set x 10-12 tekrar</p>

<h4>3. Gün: Bacaklar</h4>
<p>Squat: 4 set x 8-12 tekrar</p>
<p>Romanian deadlift: 4 set x 10-15 tekrar</p>
<p>Leg press: 3 set x 12-15 tekrar</p>
<p>Leg extensions: 3 set x 12-15 tekrar</p>
<p>Leg curls: 3 set x 12-15 tekrar</p>
<p>Calf raises: 4 set x 15-20 tekrar</p>

<h4>4. Gün: Omuzlar</h4>
<p>Overhead press: 4 set x 8-12 tekrar</p>
<p>Lateral raises: 4 set x 12-15 tekrar</p>
<p>Front raises: 3 set x 12-15 tekrar</p>
<p>Reverse flyes: 3 set x 12-15 tekrar</p>
<p>Shrugs: 3 set x 12-15 tekrar</p>

<h4>5. Gün: Kardiyo ve Karın</h4>
<p>Koşu bandında 20 dakika koşu</p>
<p>Bisiklet 20 dakika</p>
<p>Plank 3 set maksimum tekrar</p>
<p>Crunch 3 set maksimum tekrar</p>
<p>Bacak kaldırma 3 set maksimum tekrar</p>
