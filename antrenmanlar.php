            <h2>5x5 Programı</h2>
            <div class="image-placeholder"><img src="resimler/5x5.png"> </div>
            <p>5x5 programı, temel güç artışı sağlamak için geliştirilmiş bir antrenman sistemidir. 5×5 Full Body Antrenman Programı, en hızlı kas yapma, yağ yakma ve vücut geliştirme sonuçlarını sağladığı için Türkiye’nin en çok uygulanan programlarından biri haline geldi.</p>
        <h3>5x5 Açıklamaları</h3>
        <ul>
           <p> <li>Program haftada 3 gün, aralarda en az birer gün dinlenme ile uygulanmalı.</li>
            <li>5×5, 5 set 5 tekrar anlamına geliyor. 1×5 ise 1 set, 5 tekrar anlamına geliyor.</li>
            <li>Bu setler çalışma setleri, bunlardan önce normalden daha hafif ağırlıklarla 1-2 ısınma seti yapmalısınız. Antrenmanın en başında da genel bir ısınma yapmalısınız, bu da bu sayfanın alt kısmında egzersizlerle beraber bulunuyor.</li>
            <li>Setler arasında kendinizi hazır hissedene kadar (1 – 5 dakikaya kadar) dinlenebilirsiniz.</li>
            <li>Deadliftin sadece 1 set olması, sinir sistemini zorlayıcı bir egzersiz olmasından dolayıdır. 1 seti dahi efektiftir ve Deadliftin amaçlarını gerçekleştirir.</li></p>
          </ul>
          <h3>5x5 İçin Alternatif Hareketler:</h3>
          <ul>
            <li>Squat yapmanız mümkün değilse Leg Press,</li>
            <li>Bench Press yapmanız mümkün değilse Dambıl Press,</li>
            <li>Ağırlıklı Barfiks mümkün değilse Lat Pulldown,</li>
            <li>Ağırlıklı Dips mümkün değilse Close Grip Bench Press yapabilirsiniz.</li>
          </ul>
        
        
        
        
        
