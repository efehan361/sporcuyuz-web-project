<h2>Hep Bunlar mı?</h2>
<p>Dediğim gibi sıkı diyet veya temiz beslenme gibi bir hedefim yok fakat "genellikle" tükettiğim besinler bu şekilde.
    Fakat bazen dondurulmuş ürün alıp kızartırım veya hamburger yaparım. Veya bunlardan hiçbiri olmaz o gün diyete uymak istemem ve sağlıksız
    şekilde beslenirim. Bunların bir ölçüsü var abartıya kaçmamak lazım. Hazır köfte alıp kızartır pilavın üstüne de koyarım bunlar değişiklik gösterebilir.
    Diyet yapacağım diyerek hayatı kendimize zindan etmememiz gerekiyor. Hem kilo almak hem de kilo vermek için de geçerli.
</p>
<h2>Bunlar Kaç Kalori?</h2>
